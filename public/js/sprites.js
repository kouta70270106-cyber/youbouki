// 妖忘記 — 妖怪ピクセルアートスプライト生成
// 14w × 16h グリッド、PX=3 (42×48 canvas px)
// createYokaiSprites(scene) を BootScene から呼ぶ

const PX = 3;

function _p(ctx, x, y, w, h, c) {
  ctx.fillStyle = c;
  ctx.fillRect(x * PX, y * PX, w * PX, h * PX);
}

function _mkSprite(scene, key, gw, gh, fn) {
  const cvs = document.createElement('canvas');
  cvs.width  = gw * PX;
  cvs.height = gh * PX;
  fn(cvs.getContext('2d'));
  scene.textures.addCanvas(key, cvs);
}

